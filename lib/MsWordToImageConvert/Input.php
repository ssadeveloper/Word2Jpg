<?php

namespace MsWordToImageConvert;

class Input
{
    private $type;
    private $value;

    /**
     * @param int|InputType $type
     * @param string $value
     */
    public function __construct($type, $value)
    {
        $this->type = $type;
        $this->value = $value;
    }

    /**
     * @return int|InputType
     */
    public function getType()
    {
        return $this->type;
    }

    /**
     * @return string
     */
    public function getValue()
    {
        return $this->value;
    }
}